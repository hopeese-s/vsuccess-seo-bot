const { OpenAI } = require('openai');

async function generateArticle(keyword, apiKey) {
    const openai = new OpenAI({
        baseURL: 'https://api.deepseek.com',
        apiKey: apiKey
    });

    const systemPrompt = `คุณคือผู้เชี่ยวชาญด้าน Local SEO ระดับสูงและนักเขียน Content ภาษาไทยมืออาชีพ สำหรับธุรกิจ "V-Success Printing" เว็บไซต์ vsuccessprint.co.th — รับทำบัตรพนักงาน สายคล้องคอโพลีเอสเตอร์ บัตรพลาสติก และสินค้าพรีเมี่ยมครบวงจร ไม่มีขั้นต่ำ

=== ข้อมูลธุรกิจที่ต้องจำ ===
สายคล้องคอ: วัสดุโพลีเอสเตอร์ (Polyester) 100% เท่านั้น พิมพ์สกรีน/ซับลิเมชั่น สีคมชัด ไม่ตก ไม่มีเนื้อผ้าชนิดอื่น ขนาดสายมีตั้งแต่ 10, 15, 20, 25 มิลลิเมตร (10-25 มิล)
บัตรพลาสติก: บัตรพนักงาน, บัตรนักเรียน/นักศึกษา, บัตรข้าราชการ, บัตรสมาชิก (VIP Card), บัตรคีย์การ์ด (RFID/Proximity), บัตรสะสมแต้ม, บัตรโรงพยาบาล
จุดเด่น: ไม่มีขั้นต่ำ (1 ชิ้นก็ทำได้), ออกแบบฟรี, ผลิตเร็ว, ราคาโรงงาน, รับทั่วไทย ส่ง Kerry/Flash Express 3-7 วันทำการ

=== กลยุทธ์ Local SEO ที่ต้องใช้ ===
1. KEYWORD INTENT: คนต่างจังหวัดค้นหาว่า "ทำบัตรพนักงาน [จังหวัด]" เพื่อหาร้านใกล้บ้าน เราต้องอธิบายว่าสั่งออนไลน์แล้วส่งถึงจังหวัดนั้นได้
2. TITLE FORMAT: "vsuccessprint.co.th | [ประเภทสินค้า] [จังหวัด] — [จุดขาย] ส่งทั่วไทย ไม่มีขั้นต่ำ"
3. LOCAL SIGNAL: ระบุชื่อจังหวัดในเนื้อหาอย่างน้อย 5 ครั้ง เพื่อให้ Google จับ Local Signal
4. SEMANTIC CLUSTER: ครอบ Keyword รองที่เกี่ยวข้อง ต้องเน้นคำเหล่านี้ในบทความ: ร้านทำบัตรพนักงาน ใกล้ฉัน, ที่ห้อยบัตรพนักงาน, สายคล้องคอใส่บัตร, ผลิตสายคล้องคอ, รับทำสายคล้องคอ, ป้ายพนักงาน, กรอบใส่บัตรพนักงาน, ป้ายคล้องคอราคา, สายคล้องบัตรราคาถูก, ราคาสายคล้องบัตร
5. TRUST SIGNAL: กล่าวถึงประสบการณ์, ลูกค้าหลายร้อยราย, ผลิตด้วยโรงงานตัวเอง
6. STRUCTURED CONTENT: H1 > H2 > H2 > H2 > บทสรุป+CTA ยาวอย่างน้อย 800 คำ
7. CALL-TO-ACTION: ปิดท้ายด้วย CTA ชัดเจน ให้ติดต่อผ่าน LINE Official: @vsuccessprint หรือโทร: 0818483108 หรือเว็บไซต์ vsuccessprint.co.th

=== ข้อกำหนดการเขียน ===
- ใช้ภาษาไทยเป็นธรรมชาติ อ่านสบาย ไม่ฟังดูหุ่นยนต์
- ห้ามกล่าวถึงเนื้อผ้าอื่นสำหรับสายคล้องคอ นอกจากโพลีเอสเตอร์เท่านั้น
- บทความต้องยาวอย่างน้อย 800 คำ มี H2 อย่างน้อย 3 หัวข้อ
- ผลลัพธ์ต้องเป็น HTML ล้วนๆ ห้ามมี Markdown code block ห่อหุ้ม
- บรรทัดแรกต้องเป็น <h1>Title</h1> เสมอ
- ห้ามคัดลอกรูปแบบบทความเก่า ต้องเขียนใหม่ทั้งหมด`;

    const userPrompt = `เขียนบทความ SEO สำหรับ Keyword: "${keyword}"

ตัวอย่างรูปแบบ Title ที่ต้องการ (Local SEO):
- vsuccessprint.co.th | รับทำบัตรพนักงาน เชียงใหม่ — สายคล้องบัตรใกล้ฉัน ส่งทั่วประเทศ ไม่มีขั้นต่ำ
- vsuccessprint.co.th | สายคล้องคอโพลีเอสเตอร์ ขอนแก่น — ส่งทั่วประเทศ ราคาโรงงาน
- vsuccessprint.co.th | ทำบัตรนักเรียน บัตรข้าราชการ สงขลา — สายคล้องบัตรใกล้ฉัน ส่งทั่วประเทศ 1 ชิ้นก็ทำได้

โครงสร้างบทความที่ต้องเขียน:
H1: Title (ตามรูปแบบ Local SEO ด้านบน ต้องมีคำว่า "สายคล้องบัตรใกล้ฉัน" หรือ "ส่งทั่วประเทศ" รวมอยู่ด้วย)
H2: ทำไมต้องเลือก V-Success Printing? (Trust Signal + จุดเด่น)
H2: ประเภทสินค้าที่รองรับ (รายละเอียดสินค้า วัสดุ ตัวเลือก)
H2: วิธีสั่งซื้อ — ส่งตรงถึงมือใน 3-7 วันทำการ (Flow สั่งออนไลน์)
H2: FAQ คำถามที่พบบ่อย (3-4 ข้อ รูปแบบ Q: A:)
บทสรุป + CTA (ติดต่อสั่งทำ หรือสอบถามราคาผ่าน LINE Official: @vsuccessprint | โทร: 0818483108)

ข้อกำหนดพิเศษ:
- ถ้า keyword มีชื่อจังหวัด ให้ระบุจังหวัดนั้นในเนื้อหาอย่างน้อย 5 ครั้ง
- ถ้า keyword มีคำว่า "ภาค" (เช่น ภาคเหนือ, ภาคใต้, ภาคกลาง, ภาคอีสาน, ภาคตะวันออก, ภาคตะวันตก) ให้เขียนลิสต์รายชื่อจังหวัดในภาคนั้นๆ ลงในบทความให้ครบทุกจังหวัดด้วย
- ต้องมีคำว่า "สายคล้องบัตรใกล้ฉัน" และ "ส่งทั่วประเทศ" แทรกอยู่ในหัวข้อ H1 หรือ H2 อย่างเป็นธรรมชาติ
- เน้นการใช้ Keyword สำคัญเหล่านี้ในเนื้อหาอย่างเป็นธรรมชาติ: ร้านทำบัตรพนักงาน ใกล้ฉัน, ที่ห้อยบัตรพนักงาน, สายคล้องคอใส่บัตร, กรอบใส่บัตรพนักงาน, ป้ายพนักงาน, ป้ายคล้องคอราคา, สายคล้องบัตรราคาถูก
- ถ้า keyword เป็นเรื่องสายคล้องคอ ให้ยืนยันว่าวัสดุคือโพลีเอสเตอร์เท่านั้น และระบุว่ามีขนาด 10-25 มิลลิเมตร (10, 15, 20, 25 มม.)
- ถ้า keyword เป็นเรื่องบัตร ให้ระบุประเภทบัตรที่รองรับอย่างน้อย 4 ประเภท`;

    try {
        const completion = await openai.chat.completions.create({
            model: 'deepseek-chat',
            messages: [
                { role: 'system', content: systemPrompt },
                { role: 'user', content: userPrompt }
            ]
        });

        const rawOutput = completion.choices[0].message.content;

        let title = keyword;
        let content = rawOutput;

        const titleMatch = rawOutput.match(/<h1>(.*?)<\/h1>/i);
        if (titleMatch) {
            title = titleMatch[1];
            content = rawOutput.replace(/<h1>.*?<\/h1>/i, '').trim();
        }

        return { title, content };
    } catch (error) {
        console.error('DeepSeek API error:', error.message);
        throw error;
    }
}

module.exports = { generateArticle };
